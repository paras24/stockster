package com.impetus.ee.exception;

public class TeamPlannerException extends Exception 
{
	public TeamPlannerException() 
	{
		super();
	}

	public TeamPlannerException(String message, Throwable cause) 
	{
		super(message, cause);
	}

	public TeamPlannerException(String message) 
	{
		super(message);
	}

	public TeamPlannerException(Throwable cause) 
	{
		super(cause);
	}
}
